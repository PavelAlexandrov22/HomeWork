package home_work_2.loops;

public class Take1_6 {
    public static void main(String[] args) {

        for(int i = 2; i <= 9; i++){
            System.out.println("Таблица умножения");

            for(int j = 2; j <= 10; j++) {
                System.out.println( i + " x " + j + " = " + i * j );
            }
        }
    }


}
